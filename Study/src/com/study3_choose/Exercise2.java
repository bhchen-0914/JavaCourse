package com.study3_choose;


public class Exercise2 {

	public static void main(String[] args) {
		char ch='o';
				//使用switch结构判断是否为元音字母，并输出对应的信息
		switch(ch){
		case 'a':
			System.out.println("元音字母"+ch);
			break;
		case 'e':
			System.out.println("元音字母"+ch);
			break;
		case 'i':
			System.out.println("元音字母"+ch);
			break;
		case 'o':
			System.out.println("元音字母"+ch);
			break;
		case 'u':
			System.out.println("元音字母"+ch);
			break;
		default:
		    System.out.println("不是元音字母");
				}

	}

}
